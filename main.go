func main() {
	if len(os.Args) != 2 {
		fmt.Println("usage: simplewriter file.txt")
		os.Exit(1)
	}

	fname := os.Args[1]
	f, err := os.Open(fname)
	if err != nil {
		fmt.Printf("error opening file: %s, %s", fname, err)
		os.Exit(1)
	}
	defer f.Close()

	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := scanner.Text()
		words := strings.Split(line, " ")
		for _, word := range words {
			if !verifyWord(word) {
				fmt.Println(word)
			}
		}
	}
}

func verifyWord(word string) bool {
	word = strings.TrimRight(word, ".,?!)]\"")
	word = strings.TrimLeft(word, "([\"")
	return simpleWords[word]
}
